package com.core;

public class Loops {

    public static void DemoFori(){

        for (int i = 0; i <5 ; i++) {

            System.out.println(i);
        }
    }

    public static void DemoIter(){
        String [] listOfCars = {"Honda","Nissan","Ford"};
        for (String car : listOfCars) {
            System.out.println(car);

        }
    }

}
